num1 = float(input("Enter a number "))
num2 = float(input("Enter a number "))
arithmetic_operator =input("Enter an arithmetic operation: ")

if arithmetic_operator == "+" :
  print(num1 + num2)

if arithmetic_operator == "-":
  print(num1 - num2)

if arithmetic_operator == "/":
  print(num1/num2)

if arithmetic_operator == "*":
  print(num1 * num2)

if arithmetic_operator == "%":
  print(num1 % num2)

if arithmetic_operator == "//":
  print(num1//num2)

if arithmetic_operator == "**":
  print(num1 ** num2)